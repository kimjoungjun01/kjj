package step01;
public class Test05 {
  public static void main(String[] args) {
    System.out.println(100); //10진수 
    System.out.println(0144);//8진수
    System.out.println(0x64);//16진수
    System.out.println(0X64);//같은 16진수지만 소문자x를 많이 사용한다
    System.out.println(0b110_0100);//2진수
    System.out.println(0B110_0100);//2진수 역시 소문자b를 많이 사용
  }
}

